package codeUp100;

import java.util.Scanner;
public class Codeup79 {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		while(true) {
			char a = scan.next().charAt(0);
			if(a=='q') {
				System.out.println(a);
				break;
			}
			else {
				System.out.println(a);
			}
		}		
	}
}
